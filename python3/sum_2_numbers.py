# Adding two numbers in python3 


#We ask the user for two numbers and store it as a float variable
num1 = float(input("Input first number: "))
num2 = float(input("Input second number: "))

#No need the convert the numbers from string to float 

#Now lets find the sum
sum = num1 + num2

#Printing the sum
print("Num1 + Num2 =", sum)

